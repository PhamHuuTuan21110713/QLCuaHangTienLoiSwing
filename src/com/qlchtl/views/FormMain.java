    private Runnable renderStaffItem(List<NhanVien> listnv) {
        Component viewport = scpStaff.getViewport().getView();
        if (viewport instanceof JPanel) {
            scpStaff.setViewportView(null);
        }
        JPanel pnl = new JPanel();
        pnl.setLayout(null);
        pnl.setBackground(Color.white);
        pnl.setPreferredSize(new Dimension(scpStaff.getPreferredSize().width, 0));
        ItemStaff itf = new ItemStaff(this);
        Runnable a = new Runnable() {
            @Override
            public void run() {
                int col = 0;
                int row = 0;
                for (NhanVien cd :listnv) {
                    MyPanelBoxShadow pn = itf.createItemStaffComponent(col, row, cd.getMaNV(), cd.getHoTenNV(), cd.getGioiTinh(), cd.isTrangThai(), cd.getSdt(), cd.getImg());
                    if (col == 0 && row == 0) {
                        idStaffSelected = cd.getMaNV();
                        setStaffFound(cd.getHoTenNV(), cd.isTrangThai(),cd.getMaNV(),cd.getSdt(), cd.getImg());
                    }
                    pnl.add((Component) pn);
                    col++; 
                    if (col >= 1) {
                        col = 0;
                        row++;
                        SwingUtilities.invokeLater(new Runnable() {
                            public void run() {
                                int height = pnl.getPreferredSize().height + pn.getHeight() + 10;
                                pnl.setPreferredSize(new Dimension(scpStaff.getPreferredSize().width, height));
                            }
                        });
                    }

                }
                SwingUtilities.invokeLater(new Runnable() {
                    public void run() {
                        scpStaff.setViewportView(pnl);
                    }
                });
            }
        };
        return a;
    }
    

    private Runnable renderProdItem(List<SanPham> listsp) {
        Component viewport = scpProduct.getViewport().getView();
        if (viewport instanceof JPanel) {
            scpProduct.setViewportView(null);
        }
        JPanel pnl = new JPanel();
        pnl.setLayout(null);
        pnl.setBackground(Color.white);

        pnl.setPreferredSize(new Dimension(scpProduct.getPreferredSize().width,0));
        ItemProduct itpd = new ItemProduct(this);

        Runnable a = new Runnable() {
            @Override
            public void run() {
                int col = 0;
                int row = 0;
                int size = 0;
                for (SanPham cd : listsp) {

                    if(!cd.getTrangThai().equals("0")) {
                        Kho kho = khoDAo.selectById(cd.getMaSP());
                        size++;
                        MyPanelBoxShadow pn = itpd.createItemProdComponent(col, row, cd.getMaSP(), cd.getTenSP(), cd.getImg(), cd.getTienGoc(), String.valueOf(kho.getSoLuong()));
                        if (col == 0 && row == 0) {
                            idProductSelected = cd.getMaSP();
                            setProductSelected(cd.getImg(), cd.getTenSP(), cd.getTienGoc(), String.valueOf(kho.getSoLuong()));
                        }
                        pnl.add((Component) pn);
                        col++;
                        if (col > 2) {
                            col = 0;
                            row++;

                        }
                        final int finalRow = row;
                        final int finalSize = size;
                        SwingUtilities.invokeLater(new Runnable() {
                            public void run() {
                                int height = 0;
                                if(finalSize%3==0){
                                     height = finalRow * pn.getHeight() + 10 * finalRow;
                                }
                                else {
                                     height = (finalRow +1 ) * pn.getHeight() + 10 * (finalRow +1);
                                }
                                pnl.setPreferredSize(new Dimension(scpProduct.getPreferredSize().width, height));
                            }
                        });
                    }
                }
                SwingUtilities.invokeLater(new Runnable() {
                    public void run() {
                        scpProduct.setViewportView(pnl);
                    }
                });
            }
        };
        return a;
    }

    
